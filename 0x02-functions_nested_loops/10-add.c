#include "main.h"

/**
 * add - adds two integers
 *
 * Description: uses the operator +
 * to get the sum total of two integer
 * inputs
 * @a: input one of type int
 * @b: input two of type int
 *
 * Return: returns an int
 */

int add(int a, int b)
{
	int result;

	result = a + b;
	return (result);
}
